/*

题目：猴子吃桃问题：猴子第一天摘下若干个桃子，当即吃了一半，还不瘾，又多吃了一个，第二天早上
又将剩下的桃子吃掉一半，又多吃了一个。以后每天早上都吃了前一天剩下的一半零一个。到第 10 天早
上想再吃时，见只剩下一个桃子了。求第一天共摘了多少。

*/



#include "stdio.h"

void main()
{
	int day;
	int x = 0;
	int y = 1;

	for (day = 9; day > 0; day--) {
		x = (y + 1) * 2;
		y = x;
        //printf("%d, %d\n", day, y);      //逐天打印，方便检验是否正确
	}

	printf("\n\ntotal: %d\n", y);
}